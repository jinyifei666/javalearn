public class Lesson02__BaseCalculate {
    public static void main(String[] args) {

        int a = 10;
        double b = 2.5;

        // 简单的加减乘除运算
        System.out.println(a + "+" + b + "=" + (a + b));
        System.out.println(a + "-" + b + "=" + (a - b));
        System.out.println(a + "*" + b + "=" + (a * b));
        System.out.println(a + "/" + b + "=" + (a / b));
    }
}
